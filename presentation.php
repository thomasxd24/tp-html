			La majorité des matchs se terminent au bout de 25-30 minutes de jeu, mais parfois un match peut se terminer dès que
			les attrapeurs/attrapeuses rentrent sur le terrain, soit au bout de 18 minutes (ce qui est frustrant). Tout dépendra
			des compétences physiques des attrapeurs/attrapeuses et du vif-d’or. Plus ils/elles seront athlétiques, plus le
			match sera long !</p>



		<p>La capture du vif d’or ne garantit pas la victoire de l’équipe.
			Une fois le vif d’or capturé, on ajoute les 30pts de capture à l’équipe, ce qui détermine le résultat final à partir
