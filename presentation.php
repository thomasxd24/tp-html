				<ul>
					<li><b>Le départ, ou brooms up<br>
						</b>Les joueurs et joueuses sont alignés sur la ligne de départ, située à 3 mètres des buts de son équipe. Les
						balles sont positionnées au milieu du terrain&nbsp;: le souafle et un cognard à 1m de chaque côté du point
						central, les cognards restants à 7m. L’arbitre vérifie que tout le monde est prêt en s’adressant d’abord aux
						arbitres, puis à chaque équipe. Il/elle dit ensuite balai à terre, ce qui signifie que l’on doit se positionner à
						côté de son balai, genou à terre. Puis «&nbsp;prêt&nbsp;» (on doit conserver une main sur la balai mais on peut
						lever les deux genoux) et enfin « brooms up » qui signifie le début de la rencontre.</li>
				</ul>
				<ul>
					<li><b>Un match n’a pas de durée limite préétablie</b>, il se termine seulement lorsqu’un attrapeur/une attrapeuse
						arrive à&nbsp;récupérer le vif d’or (30 pts supplémentaires).<br>
						La majorité des matchs&nbsp;se terminent&nbsp;au bout de 25-30 minutes de jeu,&nbsp;mais parfois un match peut se
						terminer dès que les attrapeurs/attrapeuses rentrent sur le terrain, soit au bout de 18 minutes (ce
						qui&nbsp;est&nbsp;frustrant). Tout dépendra des&nbsp;compétences physiques des attrapeurs/attrapeuses et du
						vif-d’or. Plus ils/elles seront athlétiques, plus le match sera long !</li>
				</ul>

				<ul>
