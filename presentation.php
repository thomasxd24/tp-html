			1 vif d’or (chaussette contenant une balle de tennis) pour le/la vif-d’or.</p>

		<p>Le départ, ou brooms up
			Les joueurs et joueuses sont alignés sur la ligne de départ, située à 3 mètres des buts de son équipe. Les balles
			sont positionnées au milieu du terrain : le souafle et un cognard à 1m de chaque côté du point central, les cognards
			restants à 7m. L’arbitre vérifie que tout le monde est prêt en s’adressant d’abord aux arbitres, puis à chaque
			équipe. Il/elle dit ensuite balai à terre, ce qui signifie que l’on doit se positionner à côté de son balai, genou à
			terre. Puis « prêt » (on doit conserver une main sur la balai mais on peut lever les deux genoux) et enfin « brooms
			up » qui signifie le début de la rencontre.</p>



		<p>Un match n’a pas de durée limite préétablie, il se termine seulement lorsqu’un attrapeur/une attrapeuse arrive à
